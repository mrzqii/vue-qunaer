          </li>
        </ul>
      </div>
      <split></split>
      <div class="pics">
        <h1 class="title">商家实景</h1>
        <div class="pic-wrapper" ref="picWrapper">
          <!-- 注意这里如何动态计算ul的宽度 -->
          <ul class="pic-list" ref="picList">
            <li class="pic-item" v-for="(pic, index) in seller.pics" :key="index">
              <img :src="pic" alt="" width="120" height="90">
            </li>
          </ul>
        </div>
      </div>
      <split></split>
      <div class="info">
        <h1 class="title">商家信息</h1>
        <ul>
          <li class="info-item" v-for="(info, index) in seller.infos" :key="index">{{info}}</li>
        </ul>
      </div>
    </div>
  </div>
</template>

<script type='text/ecmascript-6'>
import star from '../star/star';
import split from '../split/split';
import BScroll from 'better-scroll';
import {saveToLocal, loadFormlocal} from '../../common/js/store.js'

export default {
  props: {
    // 通过路由传进来的
    seller:{
      type: Object
    },
  },
  data () {
    return {
      favorite: (() => {
        return loadFormlocal(this.seller.id, 'favorite', false)
      })()
    };
  },

  components: {
    star,
    split
  },

  computed: {
    favoriteText() {
      return this.favorite? '已收藏' : '收藏';
    }
  },
  created() {
    this.classMap = ['decrease', 'discount', 'special', 'invoice', 'guarantee'];
  },
  // 一个对象，键是需要观察的表达式，值是对应回调函数。值也可以是方法名，
  // 或者包含选项的对象。Vue 实例将会在实例化时调用 $watch()，遍历 watch 对象的每一个属性。
  watch: {
    seller(){
      this.$nextTick(() => {
        this._initScroll();
        this._initPics();
      });
    }
 },
 mounted() {
    this.$nextTick(() => {
      this._initScroll();
      this._initPics();
    });
  },

  methods: {
    
    _initScroll() {
      if (!this.scroll) {
        this.scroll = new BScroll(this.$refs.seller, {
          click: true
        });
      } else {
        this.scroll.refresh();
      }
    },
    _initPics() {
      if (this.seller.pics) {
        let picWidth = 120;
        let margin = 6;
        let width = (picWidth + margin) * this.seller.pics.length - margin;
        this.$refs.picList.style.width = width + 'px';
        this.$nextTick(() => {
          // 在一个已经有垂直方向的滚动里面添加水平方向的滚动
          if (!this.picScroll) {
            this.picScroll = new BScroll(this.$refs.picWrapper, {
              scrollX: true,
              eventPassthrough: 'vertical'
            });
          } else {
            this.picScroll.refresh();
          }
        });
      }
    },
    toggleFavorite(event) {
      if(!event._constructed){
        return
      };
      this.favorite = !this.favorite;
      saveToLocal(this.seller.id, 'favorite', this.favorite);

    }
  }
}

</script>
